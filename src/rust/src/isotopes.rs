#[derive(Debug)]
pub struct Isotope{
    pub mz: f64,
    pub intensity: f64,
}

const ISOTOPE_MASS: f64 = 1.002897;

pub fn isotopes (mass: f64, charge: i8, intensity: f64) -> Vec<Isotope>{

     // create the index mass to get isotopes
     let index_mass = ((mass * charge as f64) / 100.0).round() * 100.0;

     // get the isotope intensity values
     let mut iso_abn: Vec<f64> = isotope_relative_intensitys(index_mass as i64);
     let iso_abn_zero = iso_abn[0];

     iso_abn = iso_abn
               .iter_mut()
               .map(|f| ((*f / iso_abn_zero) * intensity).round())
               .collect();

      // get the index of the max intensity value
      //     let iso_abn_max_index = iso_abn
      //         .iter()
      //         .enumerate()
      //         .max_by(|a, b| a.1.partial_cmp(b.1).unwrap())
      //         .map(|(idx, _)| idx)
      //         .unwrap();

     let iso_abn_max_index = 0;

     // calculate the isotopic masses to find
     // note the offset if the tallest peak is not the monoisotope
     let iso_mz: Vec<f64> = iso_abn
         .iter()
         .enumerate()
         .map(|(a, _x)| {
             (a as f64 - iso_abn_max_index as f64) * ISOTOPE_MASS / charge as f64 + mass
         })
         .collect();

     // the main isotope vector
     iso_mz
         .iter()
         .zip(iso_abn.iter())
         .map(|(x, y)| Isotope{mz: *x, intensity: *y})
         .collect()

}

